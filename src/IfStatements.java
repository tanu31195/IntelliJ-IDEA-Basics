public class IfStatements {
    public static void main(String[] args) {
        int age = 23;
        int num = 18;

        if (age >= num && age <= 25)
        {
            System.out.println("You are over " + num);
        }
        else
        {
            System.out.println("You are below " + num);
        }
    }
}
